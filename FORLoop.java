package com.company;
import java.util.Scanner;
public class FORLoop {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int n,i;
        System.out.println("ENTER ANY NUMBER");
        n=sc.nextInt();
        for (i=200;i<=n;i++){
            System.out.println(i);

        }

    }
}
